package relationalOperators;

public class EqualOperator {

    public static void main(String[] args) {

        int chair = 7;
        int desk = 12;
        boolean result = chair == desk;

        int pen = 5;
        int pencil = 5;
        boolean size = pen == pencil;

        System.out.println(size);
        System.out.println(result);
    }
}
