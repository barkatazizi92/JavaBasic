package Class1;

public class FloatDataType {

    public static void main(String[] args) {

        // we use FloatDataType to store decimal number in the variable.

        float price = 2.99f;
        float candyPrice = 5.99f;

        System.out.println("The cookie price is " + price);
        System.out.println("The candy price is " +candyPrice);

    }
}
