package barkatExercise;

public class CompoundOperators {

    public static void main(String[] args) {
        int number= 100;
        number=number+4;
        System.out.println(number);
        number+=5;
        System.out.println(number);

        int name = 45;
        name = name/2;
        System.out.println(name);
    }
}
