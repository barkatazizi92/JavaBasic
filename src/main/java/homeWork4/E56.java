/*
create a for loop that prints out even numbers from 2 to 14 using if condition
 Must include the number 14 in the output
Output:
2
4
6
8
10
12
14
 */
public class E56 {
    public static void main(String[] args) {
        for(int i =2; i<=14; i=i+2 ){
            System.out.println(i);
        }
    }
}
