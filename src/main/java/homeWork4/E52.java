package homeWork4;
/*
Create a do while loop that prints out "Hello World!" 5 times.
Output:
Hello World!
Hello World!
Hello World!
Hello World!
Hello World!
 */
public class E52 {
    public static void main(String[] args) {
        int i =0;
        do {
            System.out.println("Hello World!");
            i++;
        }while (i<5);
    }
}
