package homeWork1;

public class E10 {
    public static void main(String[] args) {
        /*
        Declare 2 numbers and assign values 200 and 100 respectively.
        First, add numbers, then subtract, third multiply, forth divide.
         Please use variables to print the values of each operation result on a separate line. Output: 300 100 20000 2
         */
        int computerPrice = 200;
        int mobilePrice = 100;

        // first add the two numbers 200 + 100
        System.out.println(computerPrice + mobilePrice);

        // Subtract the two numbers 200 - 100
        System.out.println(computerPrice - mobilePrice);

        // multiply the two numbers 200 * 100
        System.out.println(computerPrice * mobilePrice);

        // divide the two numbers 200/100
        System.out.println(computerPrice / mobilePrice);
    }
}
