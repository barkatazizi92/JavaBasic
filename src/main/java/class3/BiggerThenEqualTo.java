package class3;

public class BiggerThenEqualTo {

    public static void main(String[] args) {
        int num1 = 10;
        int num2 =20;

        int number1 = 20;
        int number2 = 10;

        System.out.println(number1>=number2);

        System.out.println(num1>=num2);// bigger
        System.out.println(num1<=num2); //less than num2
    }
}
